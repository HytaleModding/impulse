package dev.hytalemodding.impulse.examples.commands;

import com.hypixel.hytale.component.Ref;
import com.hypixel.hytale.component.Store;
import com.hypixel.hytale.server.core.Message;
import com.hypixel.hytale.server.core.command.system.CommandContext;
import com.hypixel.hytale.server.core.command.system.basecommands.AbstractAsyncPlayerCommand;
import com.hypixel.hytale.server.core.universe.PlayerRef;
import com.hypixel.hytale.server.core.universe.world.World;
import com.hypixel.hytale.server.core.universe.world.storage.EntityStore;
import dev.hytalemodding.impulse.examples.events.PhysicsEventTracker;
import java.util.concurrent.CompletableFuture;
import javax.annotation.Nonnull;

public class EventsCommand extends AbstractAsyncPlayerCommand {

    public EventsCommand() {
        super("events", "Show Impulse physics events observed by the example ECS system");
    }

    @Nonnull
    @Override
    protected CompletableFuture<Void> executeAsync(@Nonnull CommandContext ctx,
        @Nonnull Store<EntityStore> store,
        @Nonnull Ref<EntityStore> ref,
        @Nonnull PlayerRef playerRef,
        @Nonnull World world) {
        ctx.sender().sendMessage(Message.raw(PhysicsEventTracker.snapshot()));
        return CompletableFuture.completedFuture(null);
    }
}
